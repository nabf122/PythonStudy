# 소수점이 없는 숫자는 정수형, 소수점이 있는 숫자는 실수형이라고 한다.
print("정수 : ", type(333))
print("부동소수점(실수) : ", type(1.4))

num = 15
print("num + 5 = ",num + 5)

# 숫자 연산자
# 사칙 연산자(+ - * /)
print("5 + 2 = ", 5 + 2)
print("5 - 2 = ", 5 - 2)
print("5 * 2 = ", 5 * 2)
print("5 / 2 = ", 5 / 2)
print()

# 정수 나누기 연산자( // )
print("5 // 2 = ", 5 // 2)
print()

# 나머지 연산자(%)
print("5 % 2 = ", 5 % 2)

# 제곱 연산자(**)
print("5 ** 2 = ", 5 ** 2)

